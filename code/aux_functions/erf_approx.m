function out = erf_approx(x)

k = [-1.26551223 1.00002368 0.37409196 0.09678418 -0.18628806 0.27886807 -1.13520398 1.48851587 -0.82215223 0.17087277];

t = 1./(1+0.5*abs(x));

tau = t.*exp(-x.^2-k(1)+k(2)*t+k(3)*t.^2+k(4)*t.^3+k(5)*t.^4+k(6)*t.^5+k(7)*t.^6+k(8)*t.^7+k(9)*t.^8+k(10)*t.^9);

out = (1-tau).*(x>=0);